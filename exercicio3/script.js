function soma(num1,num2){
    return num1 + num2
}
const resultadosoma = soma(+prompt("Digite um numero"),20)
console.log ("num + 20 = " + resultadosoma)

function subtração(num1,num2){
    return num1 - num2
}
const resultadoSub = subtração(+prompt("Digite um numero"),20)
console.log ("num - 20 = " + resultadoSub)

function multiplicação(num1,num2){
    return num1 * num2
}
const resultadoMult = multiplicação(+prompt("Digite um numero"),20)
console.log ("num * 20 = " + resultadoMult)

